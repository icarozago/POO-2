    private void fillTable(List<Sale> sales) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tableSales.getModel();

        while (tableSales.getRowCount() > 0) {
            defaultTableModel.removeRow(0);
        }
        
        DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");

        for (Sale sale : sales) {

            Object row[] = new Object[6];
            
            List<Ticket> tickets = sale.getTickets();
            
            List<Ticket> halfTickets = tickets.stream().filter(
                    e->e.getValue() == 8.00).collect(Collectors.toList());
            
            List<Ticket> wholeTickets = tickets.stream().filter(
                    e->e.getValue() == 16.00).collect(Collectors.toList());
            
            row[0] = tickets.get(0).getMovie().getName();
            row[1] = tickets.get(0).getSession().getTime();
            row[2] = sale.getAmount();
            row[3] = formatter.format(sale.getDate());
            row[4] = halfTickets.size();
            row[5] = wholeTickets.size();

            defaultTableModel.addRow(row);
        }
    }

